package org.example;

public class Skin {
    private int skinId;
    private int skinCost;

    public Skin(int skinId, int skinCost){

    }

    public int getSkinCost() {
        return skinCost;
    }

    public int getSkinId() {
        return skinId;
    }

    public void setSkinId(int skinId) {
        this.skinId = skinId;
    }

    public void setSkinCost(int skinCost) {
        this.skinCost = skinCost;
    }
}
